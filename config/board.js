// 小岩板点转到大岩板点的映射方法
export const convertPoint = (data) => {
    const newData = {};
    for (const name in data) {
        data[name].y = data[name].y + 6
        const boardL = JSON.parse(BOARD_COORDINATE_L)
        for (const item in boardL) {
            if (boardL[item].x === data[name].x) {
                if (boardL[item].y === data[name].y) {
                    newData[item] = { ...data[name] };
                }
            }
        }
    }
    return newData;
};

// 大岩板点转到小岩板点的映射方法
export const convertPointToM = (originData) => {
    // 削去左右6竖列的内容，只保留中间内容
    const data = {};
    for (const name in originData) {
        if (originData[name].y > 6 && originData[name].y < 30) {
            data[name] = { ...originData[name] }
        }
    }
    // 转换
    const newData = {};
    for (const name in data) {
        data[name].y = data[name].y - 6
        const boardM = JSON.parse(BOARD_COORDINATE)
        for (const item in boardM) {
            if (boardM[item].x === data[name].x) {
                if (boardM[item].y === data[name].y) {
                    newData[item] = { ...data[name] };
                }
            }
        }
    }
    return newData;
};

// 判断一个大板的数据是否可以被小板兼容显示
export const judgeBoardLCanToM = (originData) => {
    for (const name in originData) {
        if (originData[name].y <= 6 || originData[name].y >= 30) {
            return false;
        }
    }
    return true;
}

